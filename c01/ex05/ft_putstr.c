#include <unistd.h>

void	ft_putchar(char c);
void	ft_putstr(char *str);

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	size_string;

	size_string = 0;
	while (str[size_string] != '\0')
	{
		ft_putchar(str[size_string]);
		size_string++;
	}
}
